    private void btnPointActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPointActionPerformed
        // Get the current input from the viewNumberInput text field
        String input = viewNumberInput.getText();

        // Check if the input already contains a decimal point
        if (!input.contains(".")) {
            // Append a decimal point to the input
            String newInput = input + ".";
            viewNumberInput.setText(newInput);
        }
    }//GEN-LAST:event_btnPointActionPerformed

    private void btnEqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEqualActionPerformed
        // Store the second number
        number2 = Double.parseDouble(viewNumberInput.getText());

        // Perform the calculation based on the selected operator
        double result = 0;
        switch (op) {
            case "+":
                result = number1 + number2;
                break;
            case "-":
                result = number1 - number2;
                break;
            case "*":
                result = number1 * number2;
                break;
            case "/":
                result = number1 / number2;
                break;
        }

        // Display the result
        viewNumberInput.setText(String.valueOf(result));
    }//GEN-LAST:event_btnEqualActionPerformed

