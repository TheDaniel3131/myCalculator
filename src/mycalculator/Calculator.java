    private void btnIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIDActionPerformed
        // Get the current input from the viewNumberInput text field
        String input = viewNumberInput.getText();

        // Check if the input is not empty
        if (!input.isEmpty()) {
            // Check if the input already has a "+" or "-" sign
            if (input.charAt(0) == '-' || input.charAt(0) == '+') {
                // Remove the existing sign
                input = input.substring(1);
            } else {
                // Add a "-" sign in front of the input
                input = "-" + input;
            }

            // Update the viewNumberInput text field with the modified input
            viewNumberInput.setText(input);
        }
    }//GEN-LAST:event_btnIDActionPerformed

    private void btnPercentageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPercentageActionPerformed
        // Get the current input from the viewNumberInput text field
        String input = viewNumberInput.getText();

        // Convert the input to a numeric value
        double number = Double.parseDouble(input);

        // Calculate the percentage
        double percentage = number * 0.01;

        // Update the viewNumberInput text field with the percentage
        viewNumberInput.setText(Double.toString(percentage));
    }//GEN-LAST:event_btnPercentageActionPerformed

