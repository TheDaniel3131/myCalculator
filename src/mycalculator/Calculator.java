    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // Get the current text from the viewNumberInput text field
        String currentText = viewNumberInput.getText();

        // Check if there is any text to delete
        if (!currentText.isEmpty()) {
            // Remove the last character from the text
            String newText = currentText.substring(0, currentText.length() - 1);

            // Update the viewNumberInput text field with the new text
            viewNumberInput.setText(newText);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

